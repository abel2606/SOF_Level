package org.itson.sof.sof_level_presentacion.componentes;

import java.awt.BasicStroke;
import java.awt.Color;
import java.awt.Component;
import java.awt.Graphics;
import java.awt.Graphics2D;
import java.awt.Insets;
import java.awt.RenderingHints;
import javax.swing.border.Border;

/**
 *
 * @author Abe
 */
public class BordeRedondo implements Border {

    private int radius;
    private Color color;
    private int grosor;

    public BordeRedondo(int radius, Color color, int grosor) {
        this.radius = radius;
        this.color = color;
        this.grosor = grosor;
    }

    @Override
    public Insets getBorderInsets(Component c) {
        return new Insets(radius, radius, radius, radius);
    }

    @Override
    public boolean isBorderOpaque() {
        return false;
    }

    @Override
    public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
        Graphics2D g2 = (Graphics2D) g.create();
        g2.setColor(color);
        g2.setStroke(new BasicStroke(grosor));
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g2.drawRoundRect(x + grosor / 2, y + grosor / 2, width - grosor, height - grosor, radius, radius);
        g2.dispose();
    }

    
}
